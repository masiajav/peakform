## Cómo leer esta tier list

Esta guía resume el análisis del vídeo de Toniki sobre la Season 2 de Overwatch y lo convierte en decisiones prácticas para ranked. No es una regla absoluta: un jugador muy bueno puede sacar valor con picks bajos, pero los héroes altos suelen pedir menos esfuerzo para ganar peleas consistentes.

La idea principal es sencilla: el meta sigue marcado por movilidad, daño sostenido, ultimates de tempo y supports capaces de sobrevivir a Tracer, D.Va y Winston. Los héroes que dependen de setup lento o de recibir demasiado peel bajan mucho de valor.

> **Fuente**
> Análisis basado en el vídeo "MEJORES Y PEORES HÉROES SEASON 2 de OVERWATCH" de Toniki: https://www.youtube.com/watch?v=0j13vyAu0cc

## DPS: hitscan fuerte, Tracer arriba y Vendetta cae

### Tier S

- **Tracer**: sigue siendo una de las mejores opciones si tienes mecánicas y sabes atacar supports. Con perks cargadas, su daño y supervivencia la mantienen arriba.
- **Sojourn**: mucho poke, movilidad, railgun y amenaza constante. Sigue siendo una de las opciones más completas.
- **Emre**: compite directamente con Sojourn; según mapa puede ser incluso mejor.

### Tier A

- **Cassidy**: muy fuerte en mapas cerrados y distancias medias-cortas. La presión de dos disparos a cabeza y la bala de plata lo mantienen muy alto.
- **Ashe**: buena presión y mejor de lo esperado, aunque sufre cuando el rival juega Kiriko, Lucio y Tracer.
- **Pharah y Echo**: siguen fuertes como flex DPS verticales. Pharah pierde algo de velocidad aérea, pero conserva mucha amenaza.
- **Reaper**: sube mucho gracias a sus nuevas ventanas de daño y encaja especialmente bien con Zarya.

### Tier B

- **Soldier: 76 y Sierra**: picks sólidos, pero eclipsados por los hitscans superiores. Funcionan si necesitas consistencia y movilidad simple.
- **Bastion, Hanzo, Mei, Symmetra, Junkrat y Venture**: útiles en mapas o composiciones concretas. No son malos, pero dependen más del contexto.
- **Freja, Genji y Anran**: pueden funcionar, pero necesitan más ejecución o una partida que les deje jugar.

### Tier C o bajo

- **Torbjörn**: sirve contra Tracer, Genji o dives concretos, pero no marca el ritmo de la partida.
- **Widowmaker**: tiene valor en mapas largos, aunque Sojourn y Emre le quitan mucho espacio.
- **Sombra**: demasiado poco uptime; Tracer suele hacer su trabajo mejor.
- **Vendetta**: después de varios nerfs pierde armadura, alcance y facilidad para cumplir su función. Es el pick más castigado del vídeo.

## Tanks: D.Va domina y Winston/Zarya sostienen el meta

### Tier S

- **D.Va**: el tanque más fuerte del análisis. Mucho daño, movilidad, utilidad y capacidad para contestar casi todo.

### Tier A

- **Winston**: gana valor por su movilidad y por mejoras contra desplegables y escudos. Rompe mejor burbujas, torretas, pilones y barreras.
- **Zarya**: sigue arriba por la fuerza de Graviton y por su capacidad de castigar peleas largas.
- **Ramattra**: mejora mucho con su nueva forma de usar la ralentización y empieza a apuntar a pick de meta.

### Tier B

- **Roadhog**: recibe nerfs, pero mantiene valor por supervivencia y pick potential.
- **Orisa**: correcta y estable, aunque por debajo de Winston en impacto general.
- **Wrecking Ball**: puede subir mucho si se banean sus counters, pero depende demasiado de esos bans.
- **Mauga y Hazard**: útiles en matchups concretos, no como respuesta universal.

### Tier C o situacional

- **Reinhardt**: sufre contra el daño y la movilidad actual.
- **Doomfist**: viable si eres especialista, pero no destaca como pick general.
- **Junker Queen**: se expone demasiado y muere antes de convertir su presión.
- **Sigma y Domina**: pueden funcionar en mapas concretos, pero sufren contra Winston y Zarya. Domina queda especialmente lenta tras nerfs.

## Supports: Kiriko y Lúcio siguen definiendo partidas

### Tier S

- **Kiriko**: el buff a la velocidad de curación aumenta curas por segundo, carga de ulti y supervivencia propia. Kitsune sigue siendo una de las mejores ultimates.
- **Lúcio**: Speed y Sound Barrier siguen siendo herramientas de meta. Además, los boops ganan valor contra tanks que ya no reducen tanto los empujes.

### Tier A

- **Ana**: baja el cooldown de granada y vuelve a estar muy fuerte, aunque sufre contra el dúo Kiriko-Lúcio.
- **Brigitte**: más escudo, buen peel y muy buena contra D.Va y Winston.
- **Illari**: daño, sustain, ulti y supervivencia la dejan muy completa.
- **Juno**: aporta movilidad, curas, daño y buena ultimate.
- **Baptiste**: termina como A bajo por su valor global, pese a que en algunos entornos se perciba más de tier B.
- **Wuyang**: baja respecto a temporadas anteriores, pero se mantiene como A bajo por utilidad y ultimate.

### Tier B

- **Mercy**: pierde movilidad, pero el flash heal la sostiene mejor de lo que parece.
- **Zenyatta**: mejora por gestión de ultimate y Discord sigue castigando tanks como D.Va o Winston cuando no hay meta puro encima.
- **Lifeweaver**: cura mucho, el árbol es una buena ultimate y el grip gana utilidad.
- **Mizuki y Jetpack Cat**: picks de utilidad. Mizuki responde bien a Ball y dive; Jetpack Cat habilita combos concretos.

### Tier C

- **Moira**: cura y sobrevive, pero aporta poco más. En esta lectura queda por debajo de otros supports con más utilidad.

## Qué jugar si quieres subir

Si juegas DPS, prioriza **Tracer, Sojourn, Emre, Cassidy, Ashe o Reaper** antes que picks nicho. Si juegas tank, **D.Va** es la opción más completa, con **Winston**, **Zarya** y **Ramattra** como alternativas fuertes. Si juegas support, **Kiriko-Lúcio** sigue siendo la base más fiable, con **Ana**, **Brigitte**, **Illari**, **Juno** y **Baptiste** como elecciones sólidas cuando el mapa o la composición lo pidan.

La lectura más importante del vídeo no es memorizar letras, sino entender por qué suben los héroes: movilidad, supervivencia, daño aplicable y ultimates que empiezan o salvan peleas. Si tu pick no ofrece una de esas cosas de forma clara, necesitas un mapa, una composición o una mecánica muy buena para justificarlo.

